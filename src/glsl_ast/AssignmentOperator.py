from enum import Enum

class AssignmentOperator(Enum):
    Equals = '='
    PlusEquals = '+='
    MinusEquals = '-='
    StarEquals = '*='
    SlashEquals = '/='
    PercentEquals = '%='
    LeftShiftEquals = '<<='
    RightShiftEquals = '>>='
    AmpersandEquals = '&='
    CaretEquals = '^='
    PipeEquals = '|='
